define('CLI_SCRIPT', true);

require(dirname(__FILE__) . '/../../../config.php');
require_once($CFG->libdir . '/clilib.php');

if ($CFG->kent->environment !== 'dev') {
    cli_error("You cannot run this script here.");
    die("You shouldn't see this.");
}

$inputdata = file_get_contents("php://stdin");
$data = unserialize($inputdata);

foreach ($data as $table => $tabledata) {
    $DB->insert_records($table, $tabledata);
}
